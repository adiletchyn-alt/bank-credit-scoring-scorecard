import os
import pandas as pd

# 1. Указываем путь к файлу с обучающими данными
file_path = os.path.join("data", "cs-training.csv")

# 2. Читаем CSV-файл с помощью библиотеки pandas.
# index_col=0 означает, что самую первую колонку (порядковый номер ID) мы делаем индексом строк.
df = pd.read_csv(file_path, index_col=0)

# 3. Выводим размерность таблицы (сколько строк и колонок)
print(f"Размерность данных: {df.shape[0]} строк и {df.shape[1]} колонок.\n")

# 4. Проверяем, сколько пустых ячеек (пропусков) есть в каждой колонке
print("Количество пропусков по колонкам:")
print(df.isnull().sum())